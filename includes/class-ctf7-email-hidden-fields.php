<?php

/**
 * The file that defines the core plugin class
 *
 * A class definition that includes attributes and functions used across both the
 * public-facing side of the site and the admin area.
 *
 * @link       https://antonstudio.com
 * @since      1.0.0
 *
 * @package    Ctf7_Email_Hidden_Fields
 * @subpackage Ctf7_Email_Hidden_Fields/includes
 */

/**
 * The core plugin class.
 *
 * This is used to define internationalization, admin-specific hooks, and
 * public-facing site hooks.
 *
 * Also maintains the unique identifier of this plugin as well as the current
 * version of the plugin.
 *
 * @since      1.0.0
 * @package    Ctf7_Email_Hidden_Fields
 * @subpackage Ctf7_Email_Hidden_Fields/includes
 * @author     Stanley Lau <stanleyyylau@gmail.com>
 */
class Ctf7_Email_Hidden_Fields {

	/**
	 * The loader that's responsible for maintaining and registering all hooks that power
	 * the plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      Ctf7_Email_Hidden_Fields_Loader    $loader    Maintains and registers all hooks for the plugin.
	 */
	protected $loader;

	/**
	 * The unique identifier of this plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      string    $plugin_name    The string used to uniquely identify this plugin.
	 */
	protected $plugin_name;

	/**
	 * The current version of the plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      string    $version    The current version of the plugin.
	 */
	protected $version;

	/**
	 * Define the core functionality of the plugin.
	 *
	 * Set the plugin name and the plugin version that can be used throughout the plugin.
	 * Load the dependencies, define the locale, and set the hooks for the admin area and
	 * the public-facing side of the site.
	 *
	 * @since    1.0.0
	 */
	public function __construct() {
		if ( defined( 'CTF7_EMAIL_HIDDEN_FIELDS_VERSION' ) ) {
			$this->version = CTF7_EMAIL_HIDDEN_FIELDS_VERSION;
		} else {
			$this->version = '1.0.0';
		}
		$this->plugin_name = 'ctf7-email-hidden-fields';

		$this->load_dependencies();
		$this->set_locale();
		$this->define_admin_hooks();
		$this->define_public_hooks();

	}

	/**
	 * Load the required dependencies for this plugin.
	 *
	 * Include the following files that make up the plugin:
	 *
	 * - Ctf7_Email_Hidden_Fields_Loader. Orchestrates the hooks of the plugin.
	 * - Ctf7_Email_Hidden_Fields_i18n. Defines internationalization functionality.
	 * - Ctf7_Email_Hidden_Fields_Admin. Defines all hooks for the admin area.
	 * - Ctf7_Email_Hidden_Fields_Public. Defines all hooks for the public side of the site.
	 *
	 * Create an instance of the loader which will be used to register the hooks
	 * with WordPress.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function load_dependencies() {

		/**
		 * The class responsible for orchestrating the actions and filters of the
		 * core plugin.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'includes/class-ctf7-email-hidden-fields-loader.php';

		/**
		 * The class responsible for defining internationalization functionality
		 * of the plugin.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'includes/class-ctf7-email-hidden-fields-i18n.php';

		/**
		 * The class responsible for defining all actions that occur in the admin area.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'admin/class-ctf7-email-hidden-fields-admin.php';

		/**
		 * The class responsible for defining all actions that occur in the public-facing
		 * side of the site.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'public/class-ctf7-email-hidden-fields-public.php';

        /**
         * Load core classes
         */
        require_once plugin_dir_path( dirname( __FILE__ ) ) . 'admin/class-email-copy-setting.php';
        require_once plugin_dir_path( dirname( __FILE__ ) ) . 'includes/class-email-copy.php';

		$this->loader = new Ctf7_Email_Hidden_Fields_Loader();

	}

	/**
	 * Define the locale for this plugin for internationalization.
	 *
	 * Uses the Ctf7_Email_Hidden_Fields_i18n class in order to set the domain and to register the hook
	 * with WordPress.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function set_locale() {

		$plugin_i18n = new Ctf7_Email_Hidden_Fields_i18n();

		$this->loader->add_action( 'plugins_loaded', $plugin_i18n, 'load_plugin_textdomain' );

	}

	/**
	 * Register all of the hooks related to the admin area functionality
	 * of the plugin.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function define_admin_hooks() {

		$plugin_admin = new Ctf7_Email_Hidden_Fields_Admin( $this->get_plugin_name(), $this->get_version() );

		$email_copy_setting = new Email_Copy_Setting();
		$email_copy = new Email_Copy();

		$this->loader->add_action( 'admin_enqueue_scripts', $plugin_admin, 'enqueue_styles' );
		$this->loader->add_action( 'admin_enqueue_scripts', $plugin_admin, 'enqueue_scripts' );

		// admin setting page
		$this->loader->add_action('admin_menu', $email_copy_setting, 'ctfbcc_add_plugin_page');
        $this->loader->add_action('admin_init', $email_copy_setting, 'ctfbcc_page_init');

        // send copy emails
        $this->loader->add_action('wpcf7_before_send_mail', $email_copy, 'all_to_admin');

        // todo: hook only when webhook is enable
        $this->loader->add_action('cfdb7_before_save', $email_copy, 'send_cfdb7_to_web_hook');
    }

	/**
	 * Register all of the hooks related to the public-facing functionality
	 * of the plugin.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function define_public_hooks() {

		$plugin_public = new Ctf7_Email_Hidden_Fields_Public( $this->get_plugin_name(), $this->get_version() );

		$this->loader->add_action( 'wp_enqueue_scripts', $plugin_public, 'enqueue_styles' );
		$this->loader->add_action( 'wp_enqueue_scripts', $plugin_public, 'enqueue_scripts' );

	}

	/**
	 * Run the loader to execute all of the hooks with WordPress.
	 *
	 * @since    1.0.0
	 */
	public function run() {
		$this->loader->run();
	}

	/**
	 * The name of the plugin used to uniquely identify it within the context of
	 * WordPress and to define internationalization functionality.
	 *
	 * @since     1.0.0
	 * @return    string    The name of the plugin.
	 */
	public function get_plugin_name() {
		return $this->plugin_name;
	}

	/**
	 * The reference to the class that orchestrates the hooks with the plugin.
	 *
	 * @since     1.0.0
	 * @return    Ctf7_Email_Hidden_Fields_Loader    Orchestrates the hooks of the plugin.
	 */
	public function get_loader() {
		return $this->loader;
	}

	/**
	 * Retrieve the version number of the plugin.
	 *
	 * @since     1.0.0
	 * @return    string    The version number of the plugin.
	 */
	public function get_version() {
		return $this->version;
	}

}
